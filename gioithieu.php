    <li>Các sản phẩm bánh tại cở sở Sản Xuất Kinh Doanh Thực Phẩm của chúng tôi được chế biến theo công nghệ hiện đại.</br>
    Hương vị truyền thống gần gũi người Việt Nam.</li>
    <li>Với tiêu chí “<span style="color:red;">Vì sức khỏe người tiêu dùng</span>” chúng tôi cam kết:</li>
    <ul>
        <li>Cung cấp cho khách hàng những sản phẩm có giá trị dinh dưỡng cao, đảm bảo an toàn vệ sinh thực phẩm.</li>
        <li>Không ngừng cải tiến nâng cao chất lượng sản phẩm và dịch vụ để phục vụ người tiêu dùng những<br> sản phẩm an
            toàn, thơm ngon, bổ dưỡng, hình thức đẹp, hấp dẫn.</li>
        <li>Cở sở sản xuất của chúng tôi nỗ lực đa dạng hóa sản phẩm đồng thời mở rộng và khai thác ngành hàng mới,<br>
            hướng đến phục vụ nhu cầu thiết yếu hàng ngày của người tiêu dùng.</li>
    </ul>
    Hiện nay, chúng tôi cung cấp các loại bánh: Bánh tuyết bốn mùa, bánh mochi, bánh nón truyền thống, bánh lúc lắc đậu
    xanh, khoai lang tím lăn dừa,<br> bánh bao chỉ,há cảo, tôm lăn bột, tôm tẩm cốm, chả giò, …với phương châm bánh ngon mỗi ngày. <br>Cở sở sản xuất của chúng tôi sẽ là địa chỉ tin cậy của quý khách hàng.</br></br>
    <b>Sự khác biệt:</b></br>
    <ul>
        <li>Vệ sinh an toàn thực phẩm</br></li>
        <li>Giá cả hợli lý</br></li>
        <li>Sản phẩm chất lượng, uy tín</br></li>
        <li>Hình thức – đẹp</br></li>
        <li>Dịch vụ chuyên nghiệp – nhanh chóng – tiện lợi</br></li>
    </ul>
    <b>Chính sách</b></br>
    <ul>
        <li>Vận chuyển (giao hàng tận nơi cho khách hàng mua với số lượng 10 hộp trở lên, trong nội thành thành phố dưới 10km).
        </li>
        <li>Cam kết: bánh chất lượng và an toàn vệ sinh thực phẩm, hình thức đẹp, cam kết giá rẻ nhất nếu khách mua ở
            đâu rẻ hơn sẽ hoàn lại tiền.</li>
    </ul>
